package modelo;

/**
 *
 * @author Hideki
 */
public class Keyword {
    private int id;
    private String name;

    public Keyword(String name) {
        this.name = name;
    }

    public Keyword() {
    }

    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
